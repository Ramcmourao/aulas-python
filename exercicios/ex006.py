'''
Cria um programa que leia um número introduzido pelo utilizador e que mostre o seu antecessor e sucessor.
'''

numero = int(input('Digite um número: '))

antecessor = numero - 1
sucessor = numero + 1

print('O número digitado é:', numero)
print('O seu antecessor é:', antecessor)
print('O seu sucessor é:', sucessor)