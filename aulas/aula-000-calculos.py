# Regras de Prioridade em EXPRESSÕES!!!!!!!
# 1º - ()
# 2º - 2**3
# 3º - * / 
# 4º  + -

num1 = int(input('Digite o primeiro número: '))
num2 = int(input('Digite outro número: '))

soma = num1 + num2
subtracao = num1 - num2
multiplicacao = num1 * num2
divisao = num1 / num2
resto = num1 % num2

print(soma)
print(subtracao)
print(multiplicacao)
print(int(divisao))
print(resto)
